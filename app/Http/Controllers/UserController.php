     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function ajouter(Request $request)
    {
        $newUser = new User;
        $newUser->nom = $request->item['nom'];
        $newUser->prenom = $request->item['prenom'];
        $newUser->courriel = $request->item['courriel'];
        $newUser->telephone = $request->item['telephone'];
        $newUser->design = $request->item['design'];
        $newUser->emploi = $request->item['emploi'];
        $newUser->save();
        
        return $newUser;

    }

